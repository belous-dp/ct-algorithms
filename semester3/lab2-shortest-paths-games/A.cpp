void solve() {
    int n;
    cin >> n;
    vector<vector<int>> dist(n, vector<int>(n));
    for (auto& i : dist) {
        for (int& j : i) {
            cin >> j;
        }
    }
    for (int k = 0; k < n; ++k) {
        for (int i = 0; i < n; ++i) {
            for (int j = 0; j < n; ++j) {
                dist[i][j] = min(dist[i][j], dist[i][k] + dist[k][j]);
            }
        }
    }
    for (auto& i : dist) {
        for (int j : i) {
            cout << j << ' ';
        }
        cout << '\n';
    }
}